# Operadores aritméticos
# Soma
soma = 10 + 5
print(soma)
# Subtração
Subtracao = 10 - 5
print(subtracao)
# Multiplicação
multiplicacao = 10 * 5
print(multiplicacao)
# Divisão
Divisao = 10/5
print(divisao)
# Divisão inteira
Divisao_inteiro = 10 // 5
print(divisao_inteira)
# Exponenciação
exponenciacao = 10 ** 5
print (exponenciacao)
# Módulo / Resto da Divisão
modulo = 10 % 5
print(modulo)
